let vuci07 = document.getElementById(`vuci07`)

function visual() {
    let visivel = false
    if (visivel == false){
        vuci07.style.display = 'block'
        visivel = true
    } else {
        vuci07.style.display = `none`
        visivel = false
    }
}

function esconder() {
    let esconde = false
    if (esconde == false){
        vuci07.style.display = `none`
        esconde = true
    } else {
        vuci07.style.display = `block`
        esconde = false
    }
}
